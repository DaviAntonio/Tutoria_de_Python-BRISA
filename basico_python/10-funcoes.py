"""Para criar funções em python usamos a palavra reservada def 
seguida do nome da função e os parâmetros entre parenteses."""

# Funcão que retorna a soma de dois números
def soma(a, b):
    return a + b

# Para chamar a funcao basta digitar o nome da funcao e passar os parametros

print(soma(1, 2))

# Ou podemos passar os parametros com variaveis

a = 15
b = 27

print(soma(a, b))

# Ou ainda podemos atribuir o valor da funcao a uma variavel

resultado = soma(1, 2)

print(resultado)

