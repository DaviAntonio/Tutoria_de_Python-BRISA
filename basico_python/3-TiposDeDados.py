# Inicializando as variáveis e alocando o valor da multiplicação entre elas na variável 'mult'
num1 = 20
num2 = 50.00
mult = num1 * num2
print(mult)

# Qual será o tipo final da variável 'mult' ? (A função type(data), retona o tipo de dado do parametro nela passado)
print(type(mult))


num3 = 777.9
# A função round(data), aproxima um número decimal de seu inteiro mais próximo
num4 = round(num3)
# Qual será o tipo da variável 'num4' ?
print(type(num4))